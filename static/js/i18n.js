// ─── Current language (read from localStorage) ───────────────────────────────
var _currentLang = localStorage.getItem('lang') || 'en';

// ─── Translation lookup (global, used by script.js via window.t) ─────────────
window.t = function(key) {
  var lang = _currentLang;
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) ||
         (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || key;
};

// ─── Apply [data-i18n] translations across the DOM ───────────────────────────
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    // Never overwrite the button while it's in "Calculating…" state
    if (el.id === 'find-path-btn' && el.disabled) return;
    el.textContent = window.t(key);
  });

  // RTL support for Arabic
  document.documentElement.setAttribute('dir', _currentLang === 'ar' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', _currentLang);

  // Navigation page specific updates
  if (document.getElementById('hospital-map')) {
    _updateAlgoSelect();
  }
}

// ─── Rebuild algorithm <select> options in the current language ───────────────
function _updateAlgoSelect() {
  var sel = document.getElementById('algo-select');
  if (!sel) return;
  var curVal = sel.value;
  var keys = ['astar', 'idastar', 'greedy', 'ucs', 'bfs', 'dfs'];
  var vals = ['ASTAR', 'IDASTAR', 'GREEDY', 'UCS', 'BFS', 'DFS'];
  sel.innerHTML = '';
  keys.forEach(function(k, i) {
    var opt = document.createElement('option');
    opt.value = vals[i];
    opt.textContent = window.t(k);
    if (vals[i] === curVal) opt.selected = true;
    sel.appendChild(opt);
  });
}

// ─── setLanguage — exposed globally, called by the <select> onchange ──────────
window.setLanguage = function(lang) {
  if (!TRANSLATIONS[lang]) lang = 'en';
  _currentLang = lang;
  localStorage.setItem('lang', lang);

  // Sync the native <select> value
  var sel = document.getElementById('lang-select');
  if (sel) {
    sel.value = lang;
  }

  applyTranslations();
};

// ─── On load: apply saved language ───────────────────────────────────────────
window.setLanguage(_currentLang);
